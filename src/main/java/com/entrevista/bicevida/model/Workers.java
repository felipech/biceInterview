package com.entrevista.bicevida.model;

public class Workers {
    private Integer age;
    private Integer childs;

    public Workers() {
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getChilds() {
        return childs;
    }

    public void setChilds(Integer childs) {
        this.childs = childs;
    }
}
